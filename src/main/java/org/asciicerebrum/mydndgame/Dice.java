package org.asciicerebrum.mydndgame;

/**
 *
 * @author species8472
 */
public class Dice {

    private String id;
    private int sides;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the sides
     */
    public int getSides() {
        return sides;
    }

    /**
     * @param sides the sides to set
     */
    public void setSides(int sides) {
        this.sides = sides;
    }

}
